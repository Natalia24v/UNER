#7. Que imprima todos los números entre el 100 y el 199.

#Se inicia la funcion rangoCien que imprimirá el rango de números del 100 al 199
def rangoCien():
    #Se inicia un bucle for con el rango entre 100 y 200 ya que comienza el contador en 0
    for i in range(100, 200):
        #Se imprime el valor del contador por consola
        print(i)

#se llama a la funcion rangoCien para su ejecucción
rangoCien()
